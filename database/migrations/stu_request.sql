CREATE Table stu_request(
    id INT PRIMARY KEY AUTO_INCREMENT,
    code INT UNIQUE NOT NULL,
    semester_id INT NOT NULL,
    group_id INT NOT NULL,
    stu_semester_id INT NOT NULL,
    teacher INT NOT NULL,
    type INT NOT NULL,
    ira_id INT NULL,
    intern_name VARCHAR(255) NOT NULL,
    intern_phone VARCHAR(255) NOT NULL,
    intern_telephone VARCHAR(255) NOT NULL,
    from_date DATE NOT NULL,
    to_date DATE NOT NULL,
    place_name VARCHAR(255) NOT NULL,
    place_telephone VARCHAR(255) NOT NULL,
    supervisor_name VARCHAR(255) NOT NULL,
    supervisor_phone VARCHAR(255) NOT NULL,
    sat BOOLEAN NULL,
    sat_from TIMESTAMP NULL,
    sat_to TIMESTAMP NULL,
    sun BOOLEAN NULL,
    sun_from TIMESTAMP NULL,
    sun_to TIMESTAMP NULL,
    mon BOOLEAN NULL,
    mon_from TIMESTAMP NULL,
    mon_to TIMESTAMP NULL,
    wed BOOLEAN NULL,
    wed_from TIMESTAMP NULL,
    wed_to TIMESTAMP NULL,
    thu BOOLEAN NULL,
    thu_from TIMESTAMP NULL,
    thu_to TIMESTAMP NULL,
    teacher_confirm BOOLEAN NULL,
    teacher_description TEXT NULL,
    manager_confirm BOOLEAN NULL,
    manager_description TEXT NULL,
    status BOOLEAN DEFAULT 1 NULL,
    create_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NULL,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NULL,
    created_by int NULL,
    updated_by int NULL,
    Foreign Key (semester_id) REFERENCES semester(id),
    Foreign Key (group_id) REFERENCES uni_group(id),
    Foreign Key (stu_semester_id) REFERENCES stu_semesters(id),
    Foreign Key (teacher) REFERENCES users(id),
    Foreign Key (ira_id) REFERENCES intern_recruitment_application(id)
)